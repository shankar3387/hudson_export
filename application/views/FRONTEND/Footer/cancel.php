<p class="pera" style="color:black"><b>How do returns work?</b><br>
You can raise a request to return your items with these simple steps:
1. Log into your Hudson Export account
2. Go to My Orders
3. Click on 'Return' against the item you wish to return or exchange
4. Fill in the details and raise a return request
Once you raise a request, you'll get an email and SMS confirming that your request is being processed. Based on the item, your request may be automatically approved or you may be contacted for more details. If the request is approved, the item will be picked up after which you will get a replacement or refund. You can also track the status of your return request instantly from the 'My Orders' section of your Hudson Export account.
</p>



 
