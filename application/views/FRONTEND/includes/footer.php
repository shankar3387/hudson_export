function checkqtystockip(qty, proid) {
    $.ajax({
        url: '<?php echo base_url("Frontend/CartController/checkqtyipstock"); ?>',
        type: 'POST',
        dataType: "json",
        data: {
            'qty': qty,
            'proid': proid
        },
        beforeSend: function(xhr) {
            var token = $('meta[name="csrf_token"]').attr('content');
            if (token) {
                return xhr.setRequestHeader('X-CSRF-TOKEN', token);
            }
        },
        success: function(data) {
            if (data != '0') {
                $("#qtymess").hide();
                $("#button-cart").show();
                $("#showavail").show();
                $(".instock").show();
                $(".outstock").hide();
                $(".update").show();
            }
            if (data == '0') {
                $("#showavail").show();
                $("#qtymess").show();
                $("#qtymess").html("Product For The Entered Quantity Is Out Of Stock Now");
                $("#button-cart").hide();
                $(".outstock").show();
                $(".instock").hide();
                $(".update").hide();
            }
        }
    });

}

function wishlist(prdid) {
    $.ajax({
        type: "POST",
        url: '<?php echo base_url("addToWishList"); ?>',
        data: {
            prdid: prdid
        },
        processdata: false,
        cache: false,
        async: true,
        success: function(tempdata) {
            if (tempdata.trim() != "") {
                var values = JSON.parse(tempdata);
                if (values.success == "1") {
                    window.location.href = "<?php echo base_url("
                    WishList "); ?>";
                } else {
                    window.location.href = "<?php echo base_url("
                    Login "); ?>";
                }
            } else {
                $(".addtocartres" + prdid).html("Something went wrong, Please try again later.");
            }
        }
    });
}

