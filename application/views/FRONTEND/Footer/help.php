<p class="pera" style="color:black"><b>What is Cash on Delivery?</b><br>
If you are not comfortable making an online payment, you can opt for the Cash on Delivery (C-o-D) payment method instead. With C-o-D you can pay in cash at the time of actual delivery of the product at your doorstep, without requiring you to make any advance payment online.
The maximum order value for a Cash on Delivery (C-o-D) payment is $7,000.
 It is strictly a cash-only payment method. Gift Cards or store credit cannot be used for C-o-D orders.
 Foreign currency cannot be used to make a C-o-D payment. Only Indian Rupees accepted.</p>
 
<p class="pera" style="color:black"><b>How do I pay using a credit/debit card?</b><br>
We accept payments made by credit/debit cards issued in India and 21 other countries.
Credit cards
We accept payments made using Visa, MasterCard and American Express credit cards.
To pay using your credit card at checkout, you will need your card number, expiry date, three-digit CVV number (found on the backside of your card). After entering these details, you will be redirected to the bank's page for entering the online 3D Secure password.
Debit cards
We accept payments made using Visa, MasterCard and Maestro debit cards.
To pay using your debit card at checkout, you will need your card number, expiry date (optional for Maestro cards), three-digit CVV number (optional for Maestro cards). You will then be redirected to your bank's secure page for entering your online password (issued by your bank) to complete the payment.
Internationally issued credit/debit cards cannot be used for Flyte, Wallet and eGV payments/top-ups.</p>



 
<p class="pera" style="color:black"><b>Is it safe to use my credit/debit card on Hudson Export?</b><br>
Your online transaction on Hudson Export is secure with the highest levels of transaction security currently available on the Internet. Hudson Export uses 256-bit encryption technology to protect your card information while securely transmitting it to the respective banks for payment processing.
All credit card and debit card payments on Hudson Export are processed through secure and trusted payment gateways managed by leading banks.
 Banks now use the 3D Secure password service for online transactions, providing an additional
 layer of security through identity verification.</p>
 
 
 
<p class="pera" style="color:black"><b>What steps does Hudson Export take to prevent card fraud?</b><br>
Hudson Export realizes the importance of a strong fraud detection and resolution capability. We and our online payments partners monitor transactions continuously for suspicious activity and flag potentially fraudulent transactions for manual verification by our team.
In the rarest of rare cases, when our team is unable to rule out the possibility of fraud categorically,
 the transaction is kept on hold, and the customer is requested to provide identity documents.
 The ID documents help us ensure that the purchases were indeed made by a genuine card holder. 
 We apologise for any inconvenience that may be caused to customers and request them to bear with us 
 in the larger interest of ensuring a safe and secure environment for online transactions.</p>
 
  
<p class="pera"style="color:black"><b>
What is a 3D Secure password?</b><br>
The 3D Secure password is implemented by VISA and MasterCard in partnership with card issuing banks under the "Verified by VISA" and "Master card Secure Code" services, respectively.
The 3D Secure password adds an additional layer of security through identity verification for your online credit/debit card transactions. This password, which is created by you, is known only to you. This ensures that only you can use your card for online purchases.

