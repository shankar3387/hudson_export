<p class="pera" style="color:black"><b>
What is the Buyer Protection policy?
</b><br>
The Buyer Protection policy mediates buyer-seller disputes. In case a seller declines your request for a return of an item and you are not convinced with the reason given, you can write to us at hudsontech-e.com for Buyer Protection. You can dispute the resolution that 
the seller has shared for your issue until 45 days from the date of delivery and concern is looked into by us on a case-to-case basis.</p>
 
  
<p class="pera"style="color:black"><b>
What should I do if I have an issue with my product after the return period?</b><br>
You can get in touch with the brand or an authorised service centre of the brand to claim the warranty for your product (wherever applicable).
</p>
<p class="pera"style="color:black"><b>

How can I return or exchange an item?
</b><br>
To return/exchange your order, follow these simple steps:
1. Go to My Orders
2. Choose the item you wish to return or exchange
3. Fill in the details
4. Choose Request Return.
</p>
       <p class="pera"style="color:black"><b>
When is a return of an item not possible?</b><br>
Sellers cannot accept returns of item(s) in the following cases:
1. When an item is damaged because of use or when it is not in the same condition as you received it
2. When anything is missing from the package you've received including price tags, labels, original packing, freebies & accessories
3. When non-returnable items are involved such as innerwear, Lingerie etc.
4. When items are tampered with or have missing serial numbers.
</p>
     <p class="pera"style="color:black"><b>

How can I know the status of my refund?
</b><br>You can visit 'My Orders' to know the status of your refund.
For orders cancelled before shipping, refunds are processed immediately. If  the order has been shipped. refund will be processed as soon as the courier service provider confirms the return of the item(s).

</p>
        <p class="pera"style="color:black"><b>


What are the modes of refund available after cancellation?

</b><br>The different refund modes available are:
1. PhonePe Wallet - available for orders with select sellers. You will get this option for eligible orders during cancellation
2. Back to source - available for orders with all sellers. The amount is refunded to the payment mode that was originally used to pay for the order
3. NEFT transfer - available for all Cash-on-Delivery orders. As per policy, cash refunds are not possible

</p>
<p class="pera"style="color:black"><b>



When are refunds given?


</b><br>Refunds are given when:
- The seller cannot provide a replacement
- A dispute has been ruled in your favour in-line with Buyer Protection
- Sellers allow refunds on select categories under certain conditions
Please check the seller's Returns Policy on the product page for more details.

</p>
<p class="pera"style="color:black"><b>


I ordered a wrong item. Can I return it?


</b><br>The sellers' return policies don't support the return of item(s) ordered wrongly. You can refer the respective seller's Returns policy on the product page.
</p>

<p class="pera"style="color:black"><b>
What should I do if I find the package open or tampered on delivery?</b><br>
You can request for the item to be replaced by visiting 'My Orders'.
 Use the 'Return' option & fill out the details of the issue so that we can help you.

</p>
<p class="pera"style="color:black"><b>
Can I change the address for the pick-up the of item(s) in my order?</b><br>
If the pin code of the new address is serviceable for pick-up, the address can be changed while creating the return. 
The address cannot be changed in case the new address is not serviceable.

</p>
<p class="pera"style="color:black"><b>
Why have I been asked to ship the item?</b><br>
When pickup facility is not available for your location as per the courier service providers, you may be asked to ship the item back to the seller. Since the seller can arrange for a refund or a replacement only after the item reaches them, please make sure that the item is sent to the address mentioned in the return related email within 30 days of the email being sent to you.
The seller may not be able to proceed with the return request if the item does not reach the specified address within 30 days.


</p>
<p class="pera"style="color:black"><b>
What is the pickup process?</b><br>
Once your return or replacement or exchange request is accepted, the pickup of the originally delivered product will be scheduled. An SMS with more details will be sent to you on the day of the pickup.
In cases where pickup service is not available from the courier service providers at your location, you may be asked to ship the item back to the seller within 30 days of the return related email being sent to you.


</p>
<p class="pera"style="color:black"><b>

I have requested for a return for my item. When will it happen?
</b><br>
You can now track the status of your return easily right from your Hudson Export account or mobile app. Just visit the 'My Orders' page to see its status along with the date of pick-up and status of your refund if applicable.
You will also receive an email & SMS with the details of your return.


</p>
<p class="pera"style="color:black"><b>
I have requested a replacement, when will I get it?
